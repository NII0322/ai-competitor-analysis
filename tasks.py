"""任务流定义 —— 三个任务，前两个并行执行，第三个基于前两个产出撰写报告"""
from crewai import Task

from agents import market_analyst, sentiment_analyst, report_writer

# ============================================================
# 任务1：市场信息搜集（与任务2并行执行）
# ============================================================
market_research_task = Task(
    description=(
        "针对 **{product_name}** 进行全面深入的市场信息搜集。\n\n"
        "搜索维度（每个维度至少生成1个搜索词）：\n"
        "1. **商业策略与盈利模式**：收入来源、定价策略、商业化进展\n"
        "2. **市场表现与用户增长**：DAU/MAU数据、市场份额、增长率、关键里程碑\n"
        "3. **近期重大动态**：产品更新、组织架构调整、投融资事件、合作与生态布局\n"
        "4. **竞争格局**：主要竞品、差异化优势、护城河与壁垒\n"
        "5. **行业与监管**：政策动向、行业趋势、监管风险\n\n"
        "输出要求：\n"
        "- 每条关键发现需用 2-3 句话呈现，包含具体数据或事实\n"
        "- 每条发现标注信息来源和可信度（高/中/低）\n"
        "- 对信息进行去重和交叉验证，不同来源的矛盾信息要特别标注\n"
        "- 避免简单罗列链接，要提炼出有洞察的结论"
    ),
    expected_output=(
        "结构化的市场信息摘要，包含3-5个主题板块，"
        "每个板块有2-3条关键发现，每条附信息来源和可信度评估。"
    ),
    agent=market_analyst,
    async_execution=True,
)

# ============================================================
# 任务2：用户口碑分析（与任务1并行执行）
# ============================================================
sentiment_analysis_task = Task(
    description=(
        "获取 **{product_name}** 的用户口碑数据，进行专业级分析。\n\n"
        "**数据源策略（多源互补）：**\n"
        "1. 首先使用 fetch_app_reviews 工具获取 App Store 评论\n"
        "2. 如果 App Store 数据不足（评论数 < 10 或抓取失败），"
        "立即使用 search_internet 工具搜索以下方向补充：\n"
        "   - 「{product_name} 用户评价 口碑」获取综合反馈\n"
        "   - 「{product_name} 优缺点 体验」收集用户满意与不满\n"
        "   - 「{product_name} 吐槽 投诉 问题」定位核心痛点\n"
        "   - 「{product_name} 知乎 推荐」获取深度用户讨论\n"
        "3. 将所有来源的用户反馈整合去重，标注每条信息的渠道来源\n\n"
        "**分析维度：**\n"
        "1. **数据来源说明**：列出实际使用的渠道及各渠道反馈数量\n"
        "2. **整体评分画像**（如有App Store数据）：平均评分、星级分布\n"
        "3. **情感分布**：正面/负面/中性反馈的占比统计\n"
        "4. **好评Top3主题**：用户最满意的功能或体验维度，附代表性摘录\n"
        "5. **差评Top3主题**：用户最不满的问题或缺陷，附代表性摘录\n"
        "6. **高频关键词**：跨平台反馈中反复出现的关键词，按频次排序\n"
        "7. **趋势信号**：近期新出现或上升中的共性反馈"
    ),
    expected_output=(
        "用户口碑分析报告，含数据来源说明、评分画像（如有）、"
        "情感分布统计、好评/差评Top3主题及代表性摘录、"
        "高频关键词和趋势信号。即使App Store数据缺失，"
        "也必须通过Web搜索获取足够反馈完成分析。"
    ),
    agent=sentiment_analyst,
    async_execution=True,
)

# ============================================================
# 任务3：综合分析报告（依赖任务1和任务2，CrewAI 自动等待前两项完成）
# ============================================================
report_writing_task = Task(
    description=(
        "现在你已收到市场研究分析师和用户舆情分析师针对 "
        "**{product_name}** 的完整研究报告。\n\n"
        "你的任务是撰写一份达到咨询公司专业水准的竞品分析报告。"
        "这份报告将直接提交给产品副总裁和CEO作为决策参考。\n\n"
        "**报告必须严格遵循以下结构，使用规范的Markdown格式：**\n\n"
        "## 一、执行摘要\n"
        "用不超过300字概括：分析范围、核心发现、最重要的3条战略建议。"
        "高管可能只读这一部分，务必精炼有力。\n\n"
        "## 二、市场动态洞察\n"
        "- 行业趋势与市场环境概述\n"
        "- 竞品近期关键战略动作（产品迭代、商业化、组织、合作）\n"
        "- 市场表现关键数据（如有）\n"
        "- 竞争格局图景：主要玩家定位与差异化\n\n"
        "## 三、用户口碑深度分析\n"
        "- 整体评分画像与用户满意度概览\n"
        "- 好评维度Top3：用户认可的竞争力是什么（附代表性原文）\n"
        "- 差评维度Top3：用户最不满的问题是什么（附代表性原文）\n"
        "- 值得关注的趋势性信号\n\n"
        "## 四、综合研判\n"
        "将市场表现与用户口碑交叉分析：\n"
        "- 竞品的核心竞争壁垒在哪里？\n"
        "- 竞品最脆弱的环节是什么？\n"
        "- 市场数据与用户评价之间是否存在不一致？如何解释？\n"
        "- 预判竞品未来6-12个月的战略走向\n\n"
        "## 五、战略启示与机会点\n"
        "针对「我们产品」提出不少于4条可落地的战略建议：\n"
        "- 每条建议包含：行动方向、具体做法、预期收益、优先级评估\n"
        "- 用表格汇总\n\n"
        "## 六、风险提示与下一步行动\n"
        "- 分析中依赖的前提假设\n"
        "- 需要持续跟踪的关键变量\n"
        "- 建议的后续深入调研方向\n\n"
        "**写作要求：**\n"
        "- 语言专业精炼，结论有数据支撑\n"
        "- 避免模糊表述（如「可能」「或许」「大概」）\n"
        "- 使用表格呈现对比数据和行动建议\n"
        "- 关键结论用 **加粗** 突出\n"
        "- 全文不少于1500字"
    ),
    expected_output=(
        "达到咨询公司交付水准的Markdown格式竞品分析报告，"
        "包含执行摘要、市场洞察、用户口碑、综合研判、战略建议、风险提示六大板块，"
        "不少于1500字，有事实依据和独立判断，可直接作为商业决策参考。"
    ),
    agent=report_writer,
    context=[market_research_task, sentiment_analysis_task],
    markdown=True,
)
